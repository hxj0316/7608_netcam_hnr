int gk_get_max_stream_nums(void)
{
    return maxStrNums;
}

int gk_set_max_stream_nums(unsigned char strNums)
{
    if(strNums > GADI_VENC_STREAM_NUM )
    {
        LOG_ERR("set max stream nums: input invalied stream number.\n");
        return -1;
    }
    maxStrNums = strNums;
    return 0;
}

