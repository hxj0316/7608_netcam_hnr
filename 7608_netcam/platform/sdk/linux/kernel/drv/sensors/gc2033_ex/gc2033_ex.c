static int gc2033_set_agc_db(int handle, GK_Vi_SensorT* sensor_extern, s32 agc_db)
{
    int                     errorCode = 0;
    u32                     idc_reg0, idc_reg1, idc_reg2, idc_reg3;
    u32                     gain_index = GC2033_GAIN_ROWS;
    int                     i;
    GK_Vi_Sensor_GC2033T*   pdata = (GK_Vi_Sensor_GC2033T*)sensor_extern;
    //printk("[%s %d]%08x\n", __func__, __LINE__, agc_db);

    for(i = 0; i < GC2033_GAIN_ROWS; i++)
    {
        if(GC2033_GLOBAL_GAIN_TABLE[i].db_value > agc_db)
        {
            gain_index = i;
            break;
        }
    }
    if(gain_index>0)
        gain_index--;

    if (gain_index >= GC2033_GAIN_ROWS)
    {
        errorCode = -EINVAL;
        return errorCode;
    }
	//0602 hhl add print
	//printk("hhl add: gain_index=%d \n", gain_index);
    //idc_reg0 = GC2033_GLOBAL_GAIN_TABLE[gain_index].GC2033_REG_FE;
    idc_reg1 = GC2033_GLOBAL_GAIN_TABLE[gain_index].GC2033_REG_B6;
    idc_reg2 = GC2033_GLOBAL_GAIN_TABLE[gain_index].GC2033_REG_B1;
    idc_reg3 = GC2033_GLOBAL_GAIN_TABLE[gain_index].GC2033_REG_B2;
	//0602 hhl add print
    //printk("hhl add: gain: 0x%x  0x%x  0x%x\n", idc_reg1,idc_reg2,idc_reg3);
	//sensor_write_reg_ex(handle, GC2033_REG_PAGE_SET, idc_reg0, 1);    //page set
    sensor_write_reg_ex(handle, GC2033_REG_ANA_GIAN, idc_reg1, 1);    //global_gain
    sensor_write_reg_ex(handle, GC2033_REG_DIG_GIAN_IN, idc_reg2, 1); //pre_gain
    sensor_write_reg_ex(handle, GC2033_REG_DIR_GIAN_DE, idc_reg3, 1); //post_gain
    if(idc_reg1<=7)  sensor_write_reg_ex(handle, 0x21, 0x28, 1);
    else             sensor_write_reg_ex(handle, 0x21, 0x2c, 1);

    return errorCode;
}

static int gc2033_set_shutter_time(int handle, GK_Vi_SensorT* sensor_extern, u32 shutter_time, u32 extclk, u32 pclk)
{
    int     errorCode = 0;
    u64     exposure_time_q9;
	u64     exp_time;
   // u32     frame_length, line_length;
    GK_Vi_Sensor_GC2033T*   pdata = (GK_Vi_Sensor_GC2033T*)sensor_extern;
    //u32     vts1, vts2;
   //printk("[%s %d]%08x\n", __func__, __LINE__, shutter_time);

    exposure_time_q9 = shutter_time;



    /* t(frame)/t(per line) = t(exposure, in lines)*/
	exposure_time_q9 = shutter_time *(u64)pclk;
	DO_DIV_ROUND(exposure_time_q9, 2200   );
	DO_DIV_ROUND(exposure_time_q9, 512000000);


	if(exposure_time_q9 < 1 )exposure_time_q9 = 1;
	if(exposure_time_q9 > 8191 )exposure_time_q9 = 8191;
	exp_time = exposure_time_q9;
	errorCode |= sensor_write_reg_ex(handle, 0x03, ((exp_time>>8)&0x1f),1);
	errorCode |= sensor_write_reg_ex(handle, 0x04, (exp_time & 0xff),1);

    return errorCode;
}

