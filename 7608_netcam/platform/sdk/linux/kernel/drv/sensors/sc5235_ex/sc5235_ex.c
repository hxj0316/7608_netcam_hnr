static int sc5235_set_agc_db(int handle, GK_Vi_SensorT* sensor_extern, s32 agc_db)
{
    u32                     index = SC5235_GAIN_ROWS - 1;
    int                     i = 0;
    sc5235_last_pixel_update_t * sc5235_data = sensor_extern->private_data;

    for(i=0; i<SC5235_GAIN_ROWS; i++)
    {
        if(SC5235_GLOBAL_GAIN_TABLE[i].db_value > agc_db)
        {
            index = i;
            if(index > 0)
            {
                index -= 1;
            }

            break;
        }
    }

    sc5235_data->gain_update = 1;
    sc5235_data->gain = ((SC5235_GLOBAL_GAIN_TABLE[index].SC5235_REG_3E08 << 8) | SC5235_GLOBAL_GAIN_TABLE[index].SC5235_REG_3E09);
    sc5235_data->agc_db = agc_db;

    return 0;
}

static int sc5235_set_shutter_time(int handle, GK_Vi_SensorT* sensor_extern, u32 shutter_time, u32 extclk, u32 pclk)
{
    int     errorCode = 0;
    u64     exposure_time_q9;
    u32     frame_length, line_length;
    u32     shr_width_m, shr_width_l;
    sc5235_last_pixel_update_t * sc5235_data = sensor_extern->private_data;

    exposure_time_q9 = shutter_time;

    errorCode |= sensor_read_reg_ex(handle, SC5235_TIMING_VTS_H, &shr_width_m, 1);
    errorCode |= sensor_read_reg_ex(handle, SC5235_TIMING_VTS_L, &shr_width_l, 1);
    frame_length = (shr_width_m<<8) + shr_width_l;
    errorCode |= sensor_read_reg_ex(handle, SC5235_TIMING_HTS_H, &shr_width_m, 1);
    errorCode |= sensor_read_reg_ex(handle, SC5235_TIMING_HTS_L, &shr_width_l, 1);
    line_length = (((shr_width_m)<<8) + shr_width_l) * 2;  // [reg_hts] = hts /2

    /* t(frame)/t(per line) = t(exposure, in lines)*/
    exposure_time_q9 = exposure_time_q9 * (u64)pclk;// sc5235 spec: Hmax unit : INCK
    DO_DIV_ROUND(exposure_time_q9, line_length);
    DO_DIV_ROUND(exposure_time_q9, 512000000);  
    exposure_time_q9 *= 2;  //one step = 0.5 line, so 1 line MUL 2 to 0.5 line.

    if((frame_length*2 - 4) < exposure_time_q9)
    {
        exposure_time_q9 = frame_length*2 - 4;
    }


    sc5235_data->shutter_update = 1;
    sc5235_data->shutter = exposure_time_q9;

    return errorCode;
}


static int sc5235_set_update_reg(int handle, GK_Vi_SensorT* sensor_extern)
{
    int                     errorCode = 0;
    sc5235_last_pixel_update_t * sc5235_data = sensor_extern->private_data;
    /*update exposure time*/
    if(sc5235_data->shutter_update == 1)
    {
        errorCode |= sensor_write_reg_ex(handle, SC5235_AEC_EXP_TIME_V, (sc5235_data->shutter >> 12) & 0x0f, 0);
        errorCode |= sensor_write_reg_ex(handle, SC5235_AEC_EXP_TIME_H, (sc5235_data->shutter >> 4) & 0xff, 0);
        errorCode |= sensor_write_reg_ex(handle, SC5235_AEC_EXP_TIME_L, (((sc5235_data->shutter & 0xf) << 4) & 0xf0), 0);

        sc5235_data->shutter_update = 0;
    }

    /*update gain*/
    if(sc5235_data->gain_update == 1)
    {
        errorCode |= sensor_write_reg_ex(handle, SC5235_3e08, (sc5235_data->gain >> 8) & 0xff, 0);
        errorCode |= sensor_write_reg_ex(handle, SC5235_3e09, (sc5235_data->gain) & 0xff, 0);

        /*sc5235 noise logic*/
        if(sc5235_data->gain < 0x20)/*2x*/
        {
            errorCode |= sensor_write_reg_ex(handle, SC5235_3301, 0x1c, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_3633, 0x33, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_3630, 0x30, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_3622, 0xf6, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_363a, 0x87, 0);
        }
        else if(sc5235_data->gain >= 0x20 && sc5235_data->gain < 0x40)/*2x~4x*/
        {
            errorCode |= sensor_write_reg_ex(handle, SC5235_3301, 0x20, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_3633, 0x33, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_3630, 0x23, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_3622, 0xf6, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_363a, 0x87, 0);
        }
        else if(sc5235_data->gain >= 0x40 && sc5235_data->gain < 0x80)/*4x~8x*/
        {
            errorCode |= sensor_write_reg_ex(handle, SC5235_3301, 0x24, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_3633, 0x33, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_3630, 0x24, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_3622, 0xf6, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_363a, 0x9f, 0);
        }
        else if(sc5235_data->gain >= 0x80 && sc5235_data->gain < 0xfc)/*8x~15.75*/
        {
            errorCode |= sensor_write_reg_ex(handle, SC5235_3301, 0x30, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_3633, 0x33, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_3630, 0x16, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_3622, 0xf6, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_363a, 0x9f, 0);
        }
        else/*15.75~max*/
        {
            errorCode |= sensor_write_reg_ex(handle, SC5235_3301, 0x44, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_3633, 0x45, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_3630, 0x19, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_3622, 0x16, 0);
            errorCode |= sensor_write_reg_ex(handle, SC5235_363a, 0x9f, 0);
        }

        sc5235_data->gain_update = 0;
    }

    return errorCode;
}


int sc5235_probe(GK_Vi_SensorT* sensor_extern, int id)
{
    sc5235_last_pixel_update_t * sc5235_data = NULL;

    sensor_extern->private_data = kzalloc(sizeof(sc5235_last_pixel_update_t), GFP_KERNEL);
    if (sensor_extern->private_data == NULL)
    {
        printk("kzalloc memory(0x%04x) failed\n", sizeof(sc5235_last_pixel_update_t));
        return -1;
    }
    sc5235_data = sensor_extern->private_data;

    sc5235_data->sub_id = id;

    return 0;
}

int sc5235_remove(GK_Vi_SensorT* sensor_extern)
{
    if (sensor_extern->private_data)
    {
        kfree(sensor_extern->private_data);
        sensor_extern->private_data = NULL;
    }
    return 0;
}

