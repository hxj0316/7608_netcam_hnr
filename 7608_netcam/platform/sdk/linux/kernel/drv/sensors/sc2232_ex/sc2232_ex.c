static int sc2232_set_agc_db(int handle, GK_Vi_SensorT* sensor_extern, s32 agc_db)
{
    int                     errorCode = 0;
    u32                     gain_index = SC2232_GAIN_ROWS;
    int                     i;
    sc2232_last_pixel_update_t *update_ctrl = sensor_extern->private_data;

    for(i = 0; i < SC2232_GAIN_ROWS; i++)
    {
        if(SC2232_GLOBAL_GAIN_TABLE[i].db_value > agc_db)
        {
            gain_index = i-1;
            break;
        }
    }
    if (gain_index >= SC2232_GAIN_ROWS)
    {
        printk("index of gain table out of range! %d\n", gain_index);
        errorCode = -EINVAL;
        return errorCode;
    }

    //printk("agc_db 0x%08x, gain_index %02d, 0x3e08 0x%02x, 0x3e09 0x%02x\n", agc_db, gain_index,
         //SC2232_GLOBAL_GAIN_TABLE[gain_index].SC2232_REG_3E08,
         //SC2232_GLOBAL_GAIN_TABLE[gain_index].SC2232_REG_3E09);


    update_ctrl->gain_update = 1;
    update_ctrl->gain = ((SC2232_GLOBAL_GAIN_TABLE[gain_index].SC2232_REG_3E08 << 8) | SC2232_GLOBAL_GAIN_TABLE[gain_index].SC2232_REG_3E09);
    update_ctrl->agc_db = agc_db;

    return errorCode;
}

static int sc2232_set_shutter_time(int handle, GK_Vi_SensorT* sensor_extern, u32 shutter_time, u32 extclk, u32 pclk)
{
    int     errorCode = 0;
    u64     exposure_time_q9;
    u32     frame_length, line_length;
    u32     shr_width_m, shr_width_l;
    u32      gainHigh, gainLow;
    sc2232_last_pixel_update_t *update_ctrl = sensor_extern->private_data;
    
    //printk("[%s %d]%08x\n", __func__, __LINE__, shutter_time);
    exposure_time_q9 = shutter_time;

    errorCode |= sensor_read_reg_ex(handle, SC2232_TIMING_VTS_H, &shr_width_m, 1);
    errorCode |= sensor_read_reg_ex(handle, SC2232_TIMING_VTS_L, &shr_width_l, 1);
    frame_length = (shr_width_m<<8) + shr_width_l;
    errorCode |= sensor_read_reg_ex(handle, SC2232_TIMING_HTS_H, &shr_width_m, 1);
    errorCode |= sensor_read_reg_ex(handle, SC2232_TIMING_HTS_L, &shr_width_l, 1);
    line_length = ((shr_width_m)<<8) + shr_width_l;


    /* t(frame)/t(per line) = t(exposure, in lines)*/
    exposure_time_q9 = exposure_time_q9 * (u64)pclk;// sc2232 spec: Hmax unit : INCK
    DO_DIV_ROUND(exposure_time_q9, line_length);
    DO_DIV_ROUND(exposure_time_q9, (512000000/2));// exposure_time_q9*pclk*2/HTS/512000000
                                                  //=exposure_time_q9*pclk/HTS/(512000000/2)
    if((frame_length*2 - 4) < exposure_time_q9)
    {
        exposure_time_q9 = frame_length*2 - 4;
    }

    shr_width_m = (exposure_time_q9 >> 4) & 0xff;
    shr_width_l = exposure_time_q9 & 0xf;

    sensor_read_reg_ex(handle, SC2232_AEC_EXP_TIME_H, &shr_width_m, 1);
    sensor_read_reg_ex(handle, SC2232_3e08, &gainHigh, 1);
    sensor_read_reg_ex(handle, SC2232_3e09, &gainLow, 1);
    /*gain [3e08, 0x3e09]= 0x00b3 12x(21.5db)*/
    if (shr_width_m > 0x0a || ((gainHigh << 8) | gainLow) > 0x0b3)
    {
        sensor_write_reg_ex(handle, SC2232_3314, 0x02, 0);
    }
    else if (shr_width_m < 0x05)
    {
        sensor_write_reg_ex(handle, SC2232_3314, 0x12, 0);
    }


    update_ctrl->shutter_update = 1;
    update_ctrl->shutter = exposure_time_q9;
    
    return errorCode;
}


