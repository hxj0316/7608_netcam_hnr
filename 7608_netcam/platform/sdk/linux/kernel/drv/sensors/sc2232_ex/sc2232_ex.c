static int sc2232_ae_shutter_dgain_calibration(int handle, u32 pclk)
{
    unsigned int i;

    int                             errorCode = 0;
    u64                             exposure_time_q9;
    u32                             frame_length, line_length;
    u32                              shr_width_m, shr_width_l;
    printk("11frame_length %p , line_length %p\n", frame_length, line_length);

    errorCode |= sensor_read_reg_ex(handle, SC2232_TIMING_VTS_H, &shr_width_m, 0);
    errorCode |= sensor_read_reg_ex(handle, SC2232_TIMING_VTS_L, &shr_width_l, 0);
    frame_length = (shr_width_m<<8) + shr_width_l;
    errorCode |= sensor_read_reg_ex(handle, SC2232_TIMING_HTS_H, &shr_width_m, 0);
    errorCode |= sensor_read_reg_ex(handle, SC2232_TIMING_HTS_L, &shr_width_l, 0);
    line_length = ((shr_width_m)<<8) + shr_width_l;

    printk("frame_length %p , line_length %p\n", frame_length, line_length);

    for(i = 0; i < SHUTTER_TIME_TABLE_LENGTH; i++)
    {
        exposure_time_q9 = TIME_DATA_TABLE_128[i];
        /* t(frame)/t(per line) = t(exposure, in lines)*/
        exposure_time_q9 = exposure_time_q9 * (u64)pclk;// sc2232 spec: Hmax unit : INCK
        DO_DIV_ROUND(exposure_time_q9, line_length);
        DO_DIV_ROUND(exposure_time_q9, 512000000);
        if(exposure_time_q9 < 1) exposure_time_q9 = 1;
        if(exposure_time_q9 > 0xfff) exposure_time_q9 = 0xfff;

        printk("%d, 0x%x\n",
             i,exposure_time_q9);
    }

    return 0;
}
