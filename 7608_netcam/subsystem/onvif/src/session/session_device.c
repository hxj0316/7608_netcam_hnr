DeviceAdapterConfiguration_t gDeviceAdapterConfiguration =
{
	.deviceSetFactoryDefault = deviceSetFactoryDefault,
};



//*****************************************************************************
//*****************************************************************************
//** Local Functions
//*****************************************************************************
//*****************************************************************************
static GK_S32 deviceSetFactoryDefault(GK_U8 facFlag)
{
	GK_S32 retVal;

	ONVIF_Device_Cmd stCmd;
	memset(&stCmd, 0, sizeof(stCmd));
	stCmd.facFlag = facFlag;

	retVal = gk7101_Comm_CmdSend(DEVICE_SET_FACTORY_DEFAULT, sizeof(ONVIF_Device_Cmd), (GK_U8 *)&stCmd, 0, NULL);

	return retVal;
}




